package oo.abstraction;

public class VolleyballPalyer {
	String name;
	int number;
	String positions; //位置
	int height;
	int spike;  //最高打點
	int block;  //最高攔網點
	
	public VolleyballPalyer(String name,int number,String positions,int height,int spike,int block){
		this.name=name;
		this.number=number;
		this.positions= positions;
		this.height=height;
		this.spike=spike;
		this.block=block;
	}
	
	
}
